package query

///////////////////////////////////////////////////////////////////////////////
// TYPES

type query struct {
	v string
}

///////////////////////////////////////////////////////////////////////////////
// LIFECYCLE

// Q returns a new ad-hoc query
func Q(v string) *query {
	return &query{v}
}

///////////////////////////////////////////////////////////////////////////////
// STRINGIFY

// Query returns the SQL query that can be executed
func (q *query) Query() string {
	return q.v
}
