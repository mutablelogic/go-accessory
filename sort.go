package mongodb

import (
	// Packages
	bson "go.mongodb.org/mongo-driver/bson"
)

///////////////////////////////////////////////////////////////////////////////
// TYPES

type Sort struct {
	bson.D
}

///////////////////////////////////////////////////////////////////////////////
// LIFECYCLE

func S() *Sort {
	return &Sort{bson.D{}}
}

///////////////////////////////////////////////////////////////////////////////
// PRIVATE METHODS

func (sort *Sort) doc() bson.D {
	if sort == nil {
		return bson.D{}
	} else {
		return sort.D
	}
}
